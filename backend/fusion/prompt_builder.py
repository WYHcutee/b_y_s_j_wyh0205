def build_multimodal_prompt(url_features, text_content, mode='url'):
    if mode == 'url':
        prompt = f"""
你是一个网络安全专家，请综合分析以下网站信息，判断其是否为恶意网站（如钓鱼、诈骗、包含恶意软件等）。请从URL结构、网页文本内容和网页截图三个维度进行详细分析，并给出风险评分。

【URL结构分析】
{url_features}

【网页文本内容】
{text_content}

【网页截图】（已作为图像输入）

请按以下步骤分析：
1. URL风险：分析域名、路径、参数等是否存在可疑特征（如随机字符串、仿冒知名域名、异常端口等），列出具体风险点，并给出分数（0-100，越高越危险）。
2. 文本风险：分析页面文字是否存在诱导、诈骗、敏感信息（如紧急通知、账号密码输入框、虚假中奖等），列出可疑文本片段或关键词，并给出分数。
3. 图像风险：分析页面视觉布局是否模仿知名网站、是否存在异常元素（如伪造的登录框、隐藏的iframe、虚假按钮等），描述可疑区域，并给出分数。

请按以下格式输出，不要有其他解释：
URL_Score: [分数]
Text_Score: [分数]
Image_Score: [分数]
Reason: 
- URL风险分析：[详细说明，包括具体特征]
- 文本风险分析：[详细说明，包括可疑内容]
- 图像风险分析：[详细说明，包括视觉特征]
- 综合判断：[简短总结，例如“该网站高度疑似钓鱼网站”或“网站较为安全”]
"""
    elif mode == 'text':
        prompt = f"""
你是一个网络安全专家，用户只提供了网页文本内容，没有URL和截图。请根据以下文本内容分析其风险（是否包含钓鱼、诈骗、诱导等）。请详细列出文本中的可疑特征，并给出文本风险分数。URL风险和图像风险请设为0。

文本内容：
{text_content}

请按以下格式输出：
URL_Score: 0
Text_Score: [文本风险分数]
Image_Score: 0
Reason: 
- 文本风险分析：[详细说明，包括可疑关键词、句子、意图等]
- 综合判断：[简短总结]
"""
    elif mode == 'image':
        prompt = f"""
你是一个网络安全专家，用户只提供了网页截图，没有URL和文本。请根据截图分析其视觉风险（是否模仿知名网站、是否存在异常元素等）。请详细描述截图中的可疑视觉特征，并给出图像风险分数。URL风险和文本风险请设为0。

请按以下格式输出：
URL_Score: 0
Text_Score: 0
Image_Score: [图像风险分数]
Reason: 
- 图像风险分析：[详细说明，包括页面布局、元素仿冒、异常区域等]
- 综合判断：[简短总结]
"""
    else:
        prompt = ""
    return prompt